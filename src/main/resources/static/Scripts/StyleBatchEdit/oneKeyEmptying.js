function EmptyText(source) {
    var currentTr = $(source).closest("tr");
    currentTr.children("td:gt(0)").each(function () {
        var texts = $(this).find("input[type=text]");
        $(texts).each(function () {
            $(this).val("");
        });
    });
}

function EmptyRadio(source) {
    var currentTr = $(source).closest("tr");
    currentTr.children("td:gt(0)").each(function () {
        var radios = $(this).find("input[type=radio]");
        $(radios).each(function () {
            $(this).prop("checked",false);
        });
    });
}

function EmptyCheckBox(source) {
    var currentTr = $(source).closest("tr");
    currentTr.children("td:gt(0)").each(function () {
        var checkboxes = $(this).find("input[type=checkbox]");
        $(checkboxes).each(function () {
            $(this).prop("checked", false);
            $(this).prop("disabled", false);
        });
    });
}

function EmptySelect(source) {
    var currentTr = $(source).closest("tr");
    currentTr.children("td:gt(0)").each(function () {
        var selects = $(this).find("select");
        $(selects).each(function () {
            $(this).val("");
        });
    });
}

function EmptyRadioWithText(source) {
    var currentTr = $(source).closest("tr");
    currentTr.children("td:gt(0)").each(function () {
        var radios = $(this).find("input[type=radio]");
        $(radios).each(function () {
            $(this).prop("checked", false);
        });
        var texts = $(this).find("input[type=text]");
        $(texts).each(function () {
            $(this).val("");
            $(this).prop("readonly",true);
        });
    });
}

function EmptyCheckBoxWithText(source) {
    var currentTr = $(source).closest("tr");
    currentTr.children("td:gt(0)").each(function () {
        var checkboxes = $(this).find("input[type=checkbox]");
        $(checkboxes).each(function () {
            $(this).prop("checked", false);
            $(this).prop("disabled", false);
        });
        var radios = $(this).find("input[type=radio]");
        $(radios).each(function () {
            $(this).prop("checked", false);            
        });
        var texts = $(this).find("input[type=text]");
        $(texts).each(function () {
            $(this).val("");
            $(this).prop("readonly", true);
        });
    });
}